class MainActivity : ComponentActivity() {
    override fun onCreate(savedInstanceState: Bundle?) {
        super.onCreate(savedInstanceState)
        setContent {
            BasicsCodelabTheme {
                MyApp()
            }
        }
    }
}

@Composable
private fun MyApp() {
    var shouldShowOnboarding by rememberSaveable { mutableStateOf(true) }

    if (shouldShowOnboarding) {
        OnboardingScreen(onContinueClicked = { shouldShowOnboarding = false })
    } else {
        Greetings()
    }
}

@Composable
private fun OnboardingScreen(onContinueClicked: () -> Unit) {

    Surface(color = Color.DarkGray, modifier = Modifier.fillMaxSize()) {
        Column(
            modifier = Modifier.fillMaxSize(),
            verticalArrangement = Arrangement.Center,
            horizontalAlignment = Alignment.CenterHorizontally
        ) {
            // font bold 처음에 font style 에 있는건줄 착각 cause xml textStyle
            Text("Android GDG CodeLab!!", color = Color.White, fontWeight = FontWeight.Bold)

            Button(
                modifier = Modifier.padding(vertical = 24.dp),
                onClick = onContinueClicked,
                elevation = ButtonDefaults.elevation(
                    defaultElevation = 9.dp,
                    pressedElevation = 19.dp
                )
            ) {
                Text("show Lazy Column")
            }
        }
    }
}

@Composable
private fun Greetings(names: List<String> = List(1000) { "$it" }) {
    LazyColumn(modifier = Modifier.padding(vertical = 4.dp)) {
        items(items = names) { name ->
            Greeting(name = name)
        }
    }
}

@Composable
private fun Greeting(name: String) {
    Card(
        backgroundColor = MaterialTheme.colors.primary,
        modifier = Modifier.padding(vertical = 4.dp, horizontal = 8.dp)
    ) {
        ColumnContent(name)
    }
}

@Composable
private fun ColumnContent(name: String) {
    val expanded = rememberSaveable { mutableStateOf(false) }
    var input by remember { mutableStateOf("") }
    Row(
        modifier = Modifier
            .padding(12.dp)
            .animateContentSize(
                animationSpec = spring(
                    dampingRatio = Spring.DampingRatioMediumBouncy,
                    stiffness = Spring.StiffnessLow
                )
            )
    ) {
        Column(
            modifier = Modifier
                .weight(1f)
                .padding(12.dp)
        ) {
            Text(text = "Hello, ")
            Text(
                text = name,
                style = MaterialTheme.typography.h4.copy(
                    fontWeight = FontWeight.ExtraBold
                )
            )
            if (expanded.value) {
                TextField(
                    value = input,
                    onValueChange = { input = it },
                    maxLines = 1,
                    singleLine = true,
                )
            }
        }
        OutlinedButton(onClick = { expanded.value = !expanded.value }) {
            Text(color = Color.White, text = if (expanded.value) "Show less" else "Show More")
        }
    }
}

